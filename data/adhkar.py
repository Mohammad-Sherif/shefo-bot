def get_random_dhikr(used_ids: list = None) -> dict:
    """Get a random dhikr that hasn't been used today"""
    if used_ids is None:
        used_ids = []

    available = [d for d in ALL_ADHKAR if d['id'] not in used_ids and d['type'] in ('dhikr', 'hadith', 'quran')]

    if not available:
        # All used, reset
        available = [d for d in ALL_ADHKAR if d['type'] in ('dhikr', 'hadith', 'quran')]

    return random.choice(available) if available else None


def get_morning_adhkar() -> list:
    """Get morning adhkar list"""
    return [d for d in ALL_ADHKAR if d['type'] == 'morning']


def get_evening_adhkar() -> list:
    """Get evening adhkar list"""
    return [d for d in ALL_ADHKAR if d['type'] == 'evening']


def get_by_type(dhikr_type: str) -> list:
    """Filter adhkar by type: dhikr, hadith, quran, morning, evening"""
    return [d for d in ALL_ADHKAR if d['type'] == dhikr_type]
